package com.uway.mobile.domain;

public class SiteTask {

	private String id;
	private String siteId;
	private String siteWafId;
	private String siteTaskId;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSiteId() {
		return siteId;
	}
	public void setSiteId(String siteId) {
		this.siteId = siteId;
	}
	public String getSiteWafId() {
		return siteWafId;
	}
	public void setSiteWafId(String siteWafId) {
		this.siteWafId = siteWafId;
	}
	public String getSiteTaskId() {
		return siteTaskId;
	}
	public void setSiteTaskId(String siteTaskId) {
		this.siteTaskId = siteTaskId;
	}
	
	
}
